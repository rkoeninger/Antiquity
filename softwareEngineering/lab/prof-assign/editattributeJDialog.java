    public editattributeJDialog(java.awt.Frame parent, boolean modal, Database data, int mode) {
        super(parent, modal);
        initComponents();
        this.data = data;
        this.mode = mode;
        this.validator = new UIValidator(this.data);
        if (this.mode == 0) {
            this.setTitle("Edit Professor Attribute");
            int size = this.data.getNumberProfessorAttributes();
            String[] attNames = new String[size];
            for (int i = 0; i < size; i++) {
                attNames[i] = data.getProfessorAttributeByIndex(i).toString();
            }
            jComboBox1.setModel(new javax.swing.DefaultComboBoxModel(attNames));
        }
        if (this.mode == 1) {
            this.setTitle("Delete Course Attribute");
            int size = this.data.getNumberCourseAttributes();
            String[] attNames = new String[size];
            for (int i = 0; i < size; i++) {
                attNames[i] = data.getCourseAttributeByIndex(i).toString();
            }

            jComboBox1.setModel(new javax.swing.DefaultComboBoxModel(attNames));

        }
        this.setLocationRelativeTo(parent);
        this.setVisible(true);
    }

