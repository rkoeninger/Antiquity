private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
// TODO add your handling code here:
    if (jComboBox1.getSelectedIndex() != -1) {
        if (!jTextField2.getText().equals("")) {
            if (mode == 0) {
                if (validator.editProfessorAttributeProperties(jComboBox1.getSelectedItem().toString(), jTextField2.getText())) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Attribute " + jComboBox1.getSelectedItem().toString() + " has been changed.");
                    this.dispose();
                } else {
                    javax.swing.JOptionPane.showMessageDialog(this, "Can not edit attribute");
                }
            }
            if (mode == 1) {
                if (validator.editCourseAttributeProperties(jComboBox1.getSelectedItem().toString(), jTextField2.getText())) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Attribute " + jComboBox1.getSelectedItem().toString() + " has been changed.");
                    this.dispose();
                } else {
                    javax.swing.JOptionPane.showMessageDialog(this, "Can not edit attribute");
                }
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter a value.");
        }
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a attribute.");
    }

}//GEN-LAST:event_jButton1ActionPerformed

